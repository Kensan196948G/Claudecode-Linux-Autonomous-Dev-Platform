#!/usr/bin/env bash
set -euo pipefail

DEVOS_HOME="${DEVOS_HOME:-/opt/claudecode-devos}"
# shellcheck source=/dev/null
source "$DEVOS_HOME/ci/common.sh"

require_cmd git
require_cmd gh
require_cmd python3

REPO_PATH="$(ci_repo_path)"
BASE_BRANCH="$(ci_default_branch)"
ensure_repo "$REPO_PATH"
cd "$REPO_PATH"

BRANCH="repair/ci-$(date +%Y%m%d-%H%M%S)"
PROMPT_FILE="$("$DEVOS_HOME/ci/build_repair_prompt.py")"
LOG_FILE="$DEVOS_LOG_DIR/repair-ci.log"

ci_log "repair start repo=$REPO_PATH base=$BASE_BRANCH branch=$BRANCH"
git fetch origin "$BASE_BRANCH" >> "$LOG_FILE" 2>&1 || true
git checkout -B "$BRANCH" "origin/$BASE_BRANCH" >> "$LOG_FILE" 2>&1

"$DEVOS_HOME/bin/sync-claudeos-template.sh" "$(pwd)/.claude/claudeos" >> "$LOG_FILE" 2>&1 || {
  "$DEVOS_HOME/ops/state_manager.py" set decision.next_action suspend
  "$DEVOS_HOME/ops/state_manager.py" set system.last_error "ClaudeOS template sync failed during repair"
  exit 1
}
mkdir -p .git/info
touch .git/info/exclude
grep -qxF ".claude/claudeos/" .git/info/exclude || printf '%s\n' ".claude/claudeos/" >> .git/info/exclude

echo "$(date '+%F %T') [REPAIR] start branch=$BRANCH" >> "$LOG_FILE"
timeout "$CI_REPAIR_TIMEOUT_SECONDS" "$DEVOS_HOME/bin/claude-safe.sh" "$(cat "$PROMPT_FILE")" >> "$LOG_FILE" 2>&1 || true

if command -v pytest >/dev/null 2>&1; then
  if pytest -q >> "$LOG_FILE" 2>&1; then
    "$DEVOS_HOME/ops/state_manager.py" set ci.local_test_status success
  else
    "$DEVOS_HOME/ops/state_manager.py" set ci.local_test_status failure
  fi
fi
"$DEVOS_HOME/ops/harness_checks.py" --repo "$(pwd)" --phase Repair >> "$LOG_FILE" 2>&1 || true
"$DEVOS_HOME/ops/stable_gate.py" evaluate >> "$LOG_FILE" 2>&1 || true

PR_URL=""
if [[ -n "$(git status --porcelain -- ':!.claude/claudeos')" ]]; then
  git add -- . ':!.claude/claudeos'
  git commit -m "fix(ci): automated CI repair attempt" >> "$LOG_FILE" 2>&1
  git push -u origin "$BRANCH" >> "$LOG_FILE" 2>&1
  PR_URL="$(gh pr create --title "fix(ci): automated repair attempt" --body "Automated CI repair generated by DevOS" --base "$BASE_BRANCH" --head "$BRANCH" 2>> "$LOG_FILE" || true)"
else
  ci_log "repair produced no changes branch=$BRANCH"
fi

python3 - "$DEVOS_STATE_FILE" "$DEVOS_LOCK_DIR/state.lock" "$BRANCH" "$PR_URL" <<'PY'
import fcntl
import json
import sys
from datetime import datetime
from pathlib import Path

state_path = Path(sys.argv[1])
lock_path = Path(sys.argv[2])
branch = sys.argv[3]
pr_url = sys.argv[4]

with lock_path.open("w", encoding="utf-8") as lock:
    fcntl.flock(lock, fcntl.LOCK_EX)
    state = json.loads(state_path.read_text(encoding="utf-8"))
    ci = state.setdefault("ci", {})
    ci["repair_attempt_count"] = int(ci.get("repair_attempt_count") or 0) + 1
    ci["last_repair_at"] = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    ci["last_repair_branch"] = branch
    if pr_url:
        ci["last_repair_pr"] = pr_url
    state_path.write_text(json.dumps(state, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
PY

echo "$(date '+%F %T') [REPAIR] end branch=$BRANCH pr=$PR_URL" >> "$LOG_FILE"
printf '%s\n' "$PR_URL"
